TITLE TO PRINT THE STRING IN REVERSE ORDER

.MODEL SMALL
.STACK 100H
.DATA
	STR1 DB '!gnirts a si siht$'
	LENGTH DW LENGTH-STR1
.CODE
	MAIN PROC
		MOV AX,@DATA
		MOV DS,AX
		
		LEA SI,STR1
		MOV CX,LENGTH
		ADD SI,CX
		DEC SI
		DEC SI
		
		BACK:MOV DL,[SI]
		MOV AH,02H
		INT 21H
		
		DEC SI
		LOOP BACK
		
		MOV AH,4CH
		INT 21H
	MAIN ENDP
END MAIN
TITLE TO PRINT A TO Z ALPHABETS

.MODEL SMALL
.STACK 100H
.DATA
.CODE
	MAIN PROC
		MOV AX,@DATA
		MOV DS,AX
		
		MOV CX,001AH
		
		MOV DL,41H ;MOV AL,'A'
	  AGAIN:MOV AH,02H
		INT 21H
		
		
		MOV BL,DL
		MOV DL,20H ;MOV DL,' '
		MOV AH,02H
		INT 21H
		
		MOV DL,BL
		INC DL
		LOOP AGAIN
		
		MOV AH,4CH
		INT 21H
	MAIN ENDP
END MAIN
TITLE TO PRINT THE 26 APLHABETS

.MODEL SMALL
.STACK 100H
.DATA
.CODE
	MAIN PROC
		MOV AX,@DATA
		MOV DS,AX
		
		MOV CX,001AH
		
		MOV DL,'A'
		UP: MOV AH,02H
		INT 21H
		
		MOV BL,DL
		MOV DL,' '
		MOV AH,02H
		INT 21H
		
		MOV DL,BL
		INC DL
		LOOP UP
		
		MOV AH,4CH
		INT 21H
	MAIN ENDP
END MAIN
		